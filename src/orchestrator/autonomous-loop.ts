/**
 * Env var gating AL-14's worker-drain phase. Distinct from the pool-enabled
 * flag so an operator can flip the pool on (AL-12 boot/restart/shutdown
 * exercise) without automatically enabling worker spawning + drain. The
 * split exists because AL-4's merge-detection cleanup has not shipped yet:
 * every worker that reaches `awaiting-merge` state leaves its worktree on
 * disk until AL-4 lands, and we want operators to opt into that leak
 * deliberately. Default **off**.
 *
 * Recognised true-ish values: `"1"`, `"true"`, `"yes"`, `"on"`
 * (case-insensitive). Anything else — including typos — is treated as off
 * so a misconfigured flag doesn't silently leak worktrees.
 */
export const RELAY_AL14_WORKER_DRAIN = "RELAY_AL14_WORKER_DRAIN";

/**
 * Parse {@link RELAY_AL14_WORKER_DRAIN} into a bool. See the constant's
 * docstring for the "why two flags" rationale.
 */
export function isWorkerDrainEnabled(env: NodeJS.ProcessEnv = process.env): boolean {
  const raw = env[RELAY_AL14_WORKER_DRAIN];
  if (!raw) return false;
  const normalized = raw.trim().toLowerCase();
  return normalized === "1" || normalized === "true" || normalized === "yes" || normalized === "on";
}

/**
 * Options handed to {@link startAutonomousSession} by the CLI entrypoint
 * (`rly run --autonomous`, AL-3). AL-3 constructs the session scaffolding —
 * lifecycle, token tracker, audit decision entry, allowed-repo filter — and
 * then delegates actual ticket dispatch + driver loop to the function below.
 *
 * AL-4 owns the steady-state driver. When `RELAY_REPO_ADMIN_POOL_ENABLED`
 * and `RELAY_AL14_WORKER_DRAIN` are both on, the driver boots the pool,
 * routes ready tickets via {@link TicketRouter}, drains each admin's queue
 * in parallel via {@link TicketRunner}, and loops until the board has no
 * more work or the lifecycle has been kicked to `winding_down` / `killed`.
 *
 * Other combinations preserve the earlier-ticket behaviour (AL-12 /
 * AL-13 path): single-pass routing without drain, or no pool at all.
 */
export interface StartAutonomousSessionOptions {
  /** Session id generated by the CLI; shared by tracker + lifecycle + audit. */
  sessionId: string;
  /** The channel whose ticket board this session executes against. */
  channel: Channel;
  /** Token-budget tracker. Caller keeps ownership; AL-4 records against it. */
  tracker: TokenTracker;
  /** Lifecycle state machine. Caller has already transitioned it into
   * `dispatching`; AL-4's driver owns subsequent transitions. */
  lifecycle: SessionLifecycle;
  /** Trust mode as selected on the CLI. AL-7 supplies the trust-mode gate
   * (`src/approvals/trust-gate.ts`) that every ack-requiring call site (PR
   * auto-merge from AL-5, audit-proposed ticket from AL-6) threads this
   * value into. Under `supervised`, ack-requiring actions are written to
   * `~/.relay/approvals/<sessionId>/queue.jsonl`; under `god` with
   * `RELAY_AL7_GOD_AUTOMERGE=1`, they execute directly. See
   * {@link "../approvals/trust-gate".decide}. */
  trust: "supervised" | "god";
  /** Repo assignments the session is allowed to dispatch work into. Either
   * the subset selected by `--allow-repo` or — when the flag is absent —
   * every assignment on the channel. AL-4 uses this as a filter at dispatch
   * time. */
  allowedRepos: RepoAssignment[];
  /**
   * AL-9: poll cadence for the STOP-file kill switch. The driver checks
   * `~/.relay/sessions/<sessionId>/STOP` before each dispatch tick and
   * transitions the lifecycle to `winding_down` when the file appears.
   * Defaults to {@link DEFAULT_STOP_POLL_INTERVAL_MS} (20s) matching the
   * AL-9 acceptance criterion "within one tick (≤20s)". Tests override
   * to a small value so the stop signal is picked up quickly.
   */
  stopPollIntervalMs?: number;
  /**
   * **Test-only** injection hooks. Production callers MUST NOT set these —
   * they exist solely so the AL-14 drain integration test can exercise the
   * pool-boot + router + runner drain path end-to-end without a real
   * `claude` binary. Four seams are exposed:
   *
   *  - `channelStore`: replaces the default `new ChannelStore()` so tests
   *    can drive a tmp-dir-backed store with seeded channels + tickets.
   *  - `repoAdminSpawner`: threaded into the `RepoAdminPool` so its per-
   *    admin children are fakes (no real CLI).
   *  - `workerSpawner`: replaces `new WorkerSpawner()` so the runner's
   *    spawn + monitor loop fires deterministically against a fake.
   *  - `rootDir`: overrides the pool's `~/.relay` base dir.
   *  - `pollIntervalMs`: shortens the driver's steady-state poll interval
   *    so tests don't wait real wall-clock seconds between cycles. Omitted
   *    in production (defaults to {@link DEFAULT_POLL_INTERVAL_MS}).
   *
   * All five are optional and ALL must be `undefined` in production.
   * Presence of any field constitutes a test invocation.
   */
  testOverrides?: {
    channelStore?: ChannelStore;
    repoAdminSpawner?: RepoAdminProcessSpawner;
    workerSpawner?: WorkerSpawner;
    /**
     * Override the pool's `~/.relay` base dir so the test's tmp dir is
     * used instead of the real home dir for session/admin logs. Also
     * used by {@link checkForStop} so the AL-9 kill-switch integration
     * test can drop a STOP file in the test's tmp dir.
     */
    rootDir?: string;
    /**
     * AL-6: replace the audit agent's LLM invoker with a deterministic
     * scripted function. Tests use this to return pre-shaped JSON so the
     * gating + decision-write path runs end-to-end without a real CLI
     * spawn. Production leaves this unset; `runPostCompletionAudit` is
     * never reached in pre-AL-4 shapes (the driver stub skips it) so the
     * production default is "no invoker, no audit" until AL-4 wires in a
     * factory-built agent.
     */
    auditInvoker?: AuditInvoker;
    /**
     * AL-4: poll interval between steady-state driver passes when there are no
     * `ready` tickets on the board and the runners are still draining.
     * Tests set this to 1–5ms so the loop tightens on async progress
     * instead of stalling on the default one-second cadence.
     */
    pollIntervalMs?: number;
    /**
     * AL-14 follow-up: optional pr-poller hook injected so the driver can
     * subscribe to merge events. When set, the driver registers an
     * `onMerged` listener; each merged PR is routed to the matching
     * {@link TicketRunner.handlePrMerged} so the worktree is destroyed
     * within one poll tick of the merge (AC1 of the AL-14 follow-up).
     *
     * Production callers are expected to pass a live {@link PrPoller};
     * tests inject a fake that fires `onMerged` synchronously so the
     * invariant ("merge event → worktree destroyed") can be asserted
     * without a real GitHub round-trip.
     */
    prPoller?: Pick<PrPoller, "onMerged">;
    /**
     * AL-14 follow-up: test seam for the terminal sweep pass. When set,
     * replaces the default `gh pr view` probe with a fake so tests can
     * assert the driver's terminal-sweep behaviour without shelling out.
     */
    ghPrView?: GhPrView;
  };
  /**
   * AL-6 seam: invoked exactly once when the driver reaches "all
   * tickets done" — after the last ticket finishes draining and before
   * the lifecycle transitions to its terminal state. AL-4's real driver
   * is expected to populate this; until AL-4 ships, the default
   * behaviour (defined below) is to directly invoke
   * {@link runPostCompletionAudit} against the channel's ledger. Callers
   * that want to suppress the audit entirely can pass a no-op.
   *
   * The hook runs synchronously inside the loop's post-drain block: a
   * throw here is caught and warned, never rethrown, because the
   * autonomous loop's shutdown path must be robust to third-party
   * failures.
   */
  onAllTicketsComplete?: (ctx: AllTicketsCompleteContext) => Promise<void>;
}

/**
 * Context handed to the {@link StartAutonomousSessionOptions.onAllTicketsComplete}
 * seam. Everything the seam needs to run AL-6's
 * {@link runPostCompletionAudit} is on this object; no "reach into the
 * loop's internals" required.
 */
export interface AllTicketsCompleteContext {
  channel: Channel;
  channelStore: ChannelStore;
  sessionId: string;
  tracker: TokenTracker;
  /**
   * Post-drain budget headroom percentage (0..100). Pre-computed so the
   * seam implementation doesn't have to care whether the tracker has
   * replayed its JSONL yet.
   */
  budgetHeadroomPct: number;
  /**
   * Audit invoker threaded through from `testOverrides.auditInvoker`, if
   * any. Production is `undefined` — AL-4's real driver is expected to
   * build one from the agent factory.
   */
  auditInvoker?: AuditInvoker;
}

/**
 * Default poll interval for the AL-4 steady-state driver loop. One second is
 * a compromise between wall-clock responsiveness (pick up a newly-ready
 * ticket within a second of it landing) and not burning CPU in the common
 * case where the board is static.
 *
 * Tests override this via `testOverrides.pollIntervalMs` so async drain
 * assertions don't wait real-time seconds between passes.
 */
export const DEFAULT_POLL_INTERVAL_MS = 1_000;

/**
 * Terminal reasons the AL-4 driver records on the lifecycle transition.
 * Each is surfaced in `~/.relay/sessions/<id>/lifecycle.json` so a post-
 * mortem can tell why the autonomous loop stopped. The set is intentionally
 * small — individual failure modes (runner throw, pool flap, router block)
 * don't justify a terminal-reason proliferation since each already has a
 * per-ticket surface on the channel feed.
 */
type DriverTerminalReason =
  | "done"
  | "budget-winding-down"
  | "wall-clock-exceeded"
  | "killed-externally"
  | "al-13-pending"
  | "al-14-pending"
  // AL-9: user-stop signal from the STOP file. The pre-routing poll (or
  // the background poll arming during drain) transitions the lifecycle
  // to `winding_down` with this reason — the driver observes it via its
  // `onTransition` sub AND the initial-state check at entry so a STOP
  // landing before the driver starts is honored.
  | "user-stop-signal";

/**
 * Autonomous-loop driver entrypoint. The behaviour is gated by two env
 * flags so AL-12's pool lifecycle and AL-14's worker drain can be rolled
 * out independently:
 *
 *   1. When {@link RELAY_REPO_ADMIN_POOL_ENABLED} is off:
 *      Preserves the pre-AL-12 behaviour — no pool, no routing, lifecycle
 *      transitions to `killed` with reason `"al-13-pending"`. This is the
 *      safe default.
 *   2. When only the pool flag is on:
 *      Boots the repo-admin pool (AL-12) and routes each ready ticket
 *      through {@link TicketRouter} (AL-13) so the matching admin queues
 *      it. Workers are NOT spawned and queues are NOT drained — the
 *      lifecycle transitions to `killed` with reason `"al-14-pending"`.
 *      This mode exercises boot + routing in production without leaking
 *      worktrees.
 *   3. When BOTH {@link RELAY_REPO_ADMIN_POOL_ENABLED} and
 *      {@link RELAY_AL14_WORKER_DRAIN} are on:
 *      **AL-4 steady-state driver.** Boots pool + coordinator, routes
 *      ready tickets, drains each admin's queue in parallel, and
 *      re-polls for newly-ready tickets each cycle. Exits:
 *        - `done / "done"` when the board has no `ready` tickets left
 *          and every runner is idle (all tickets reached terminal state:
 *          `verifying` / `completed` / `failed` / `blocked`).
 *        - `done / "budget-winding-down"` when the 85% token threshold
 *          flipped the lifecycle to `winding_down` mid-drain. In-flight
 *          tickets finish; no new tickets are dispatched after the flip.
 *        - `killed / "wall-clock-exceeded"` when AL-2's wall-clock
 *          watchdog fired. Lifecycle already transitioned itself; the
 *          driver observes, signals in-flight workers, and exits.
 *        - `killed / "killed-externally"` when something else (budget
 *          95%, signal handler) transitioned the lifecycle to `killed`.
 *   4. Tears down pool + lifecycle + tracker. Errors during teardown are
 *      logged and swallowed: a cosmetic shutdown failure must not turn a
 *      successful handoff into a non-zero CLI exit.
 *
 * ## Why two flags
 *
 * AL-14 owns spawn + drain but NOT PR-merge cleanup (AL-4's scope). A
 * ticket whose worker opens a PR transitions to `awaiting-merge` and its
 * worktree persists on disk until a merge event reaches
 * {@link TicketRunner.handlePrMerged}. Until AL-4 ships merge-detection
 * wiring, every pool-enabled autonomous run with the drain on leaks one
 * worktree per successful ticket. Gating the drain behind its own flag
 * lets operators opt into that leak knowingly — and keeps a clean opt-
 * out path for AL-12/AL-13 exercise runs that shouldn't spawn workers at
 * all.
 *
 * PR-merge cleanup (AC3) is NOT driven here — the runner exposes
 * {@link TicketRunner.handlePrMerged}; AL-5's PR reviewer / pr-watcher
 * will call it when a merge event lands. AL-4's steady-state driver just
 * needs to NOT block on PRs merging (tickets in `verifying` / awaiting-
 * merge are treated as terminal for the purposes of driver exit).
 *
 * ## What AL-4 deliberately does NOT do
 *
 * - **AL-5 PR review**: the driver does not spawn a reviewer when a PR
 *   opens. AL-5 plugs into the runner's `worker-pr-opened` event.
 * - **AL-6 audit**: the driver transitions to `done` directly from
 *   `winding_down`. AL-6 will later hook into the pre-done path to run
 *   an audit pass (via the `winding_down → audit → done` edge) before
 *   terminal transition; AL-4 keeps the hook surface open by using
 *   `transition("winding_down")` then `transition("done")` as two
 *   separate awaits.
 * - **AL-9 STOP file**: kill-switch polling is AL-9's scope. AL-4 does
 *   not watch for a file sentinel.
 */
export async function startAutonomousSession(opts: StartAutonomousSessionOptions): Promise<void> {
  const { sessionId, lifecycle, tracker, channel, allowedRepos, testOverrides } = opts;
  const stopPollIntervalMs = opts.stopPollIntervalMs ?? DEFAULT_STOP_POLL_INTERVAL_MS;

  // AL-9: transient flag so teardown can branch on whether we're
  // unwinding because of a user stop signal vs. normal terminal. The
  // lifecycle transition is the source of truth for downstream readers;
  // this flag only helps the local teardown log message.
  let userStopped = false;

  /**
   * AL-9 — poll the STOP file for this session. When the file is
   * present, transition the lifecycle to `winding_down` (if we're in
   * `dispatching`) with reason `"user-stop-signal"`. The transition is
   * idempotent — calling this helper again after the first signal is
   * observed is a no-op. Errors from the filesystem stat are logged
   * but NOT fatal: a flaky `~/.relay` shouldn't force-kill an
   * in-progress session, the wall-clock watchdog + token budget are
   * the hard stops. The STOP file is an advisory soft stop.
   */
  async function pollForStopAndWindDown(): Promise<boolean> {
    if (userStopped) return true;
    let present = false;
    try {
      present = await checkForStop(sessionId, testOverrides?.rootDir);
    } catch (err) {
      console.warn(
        `[autonomous-loop] STOP-file check failed for session ${sessionId}: ${
          err instanceof Error ? err.message : String(err)
        }`
      );
      return false;
    }
    if (!present) return false;
    userStopped = true;
    const state = lifecycle.state;
    // Only `dispatching` has an edge to `winding_down`. From `planning`
    // we short-circuit to `killed` — we never started dispatching so
    // the wind-down drain has nothing to do. From `winding_down` /
    // `audit` we leave the state alone; the kill switch already fired
    // or a later transition has taken over.
    if (state === "dispatching") {
      try {
        await lifecycle.transition("winding_down", STOP_FILE_REASON);
      } catch (err) {
        console.warn(
          `[autonomous-loop] failed to transition to winding_down on STOP for session ${sessionId}: ${
            err instanceof Error ? err.message : String(err)
          }`
        );
      }
    } else if (state === "planning") {
      try {
        await lifecycle.transition("killed", STOP_FILE_REASON);
      } catch (err) {
        console.warn(
          `[autonomous-loop] failed to transition to killed on STOP for session ${sessionId}: ${
            err instanceof Error ? err.message : String(err)
          }`
        );
      }
    }
    console.log(
      `[autonomous-loop] STOP file observed for session ${sessionId}; winding down gracefully (poll cadence ${stopPollIntervalMs}ms).`
    );
    return true;
  }

  // AL-12 built the repo-admin pool, but the admin-process handshake
  // protocol it relies on is AL-14's deliverable. Without that protocol
  // the default spawner's child exits in ms (no prompt, stdin closed)
  // and the pool flaps until the rapid-restart ceiling fires. Gate pool
  // construction behind RELAY_REPO_ADMIN_POOL_ENABLED so production
  // `rly run --autonomous` invocations keep the pre-AL-12 behaviour
  // (clean `killed / al-13-pending` exit) until AL-14 flips the flag.
  const poolEnabled = isRepoAdminPoolEnabled();
  // AL-14 spawn + drain is gated by a SEPARATE flag from the pool flag.
  // Until AL-4 ships merge-detection cleanup, every ticket that reaches
  // `awaiting-merge` leaves a worktree on disk — the two-flag split lets
  // operators opt into that leak explicitly rather than inheriting it
  // by flipping the pool flag. When the drain flag is off but the pool
  // flag is on, the loop still routes tickets (AL-13) but no workers
  // are spawned, and the lifecycle exits with reason `al-14-pending`.
  const drainEnabled = poolEnabled && isWorkerDrainEnabled();

  if (!poolEnabled) {
    console.log(
      `[autonomous-loop] dispatcher not yet implemented — ` +
        `Session ${sessionId} marked as killed with reason "al-13-pending".`
    );
  } else if (!drainEnabled) {
    console.log(
      `[autonomous-loop] repo-admin pool + router enabled; worker drain disabled ` +
        `(RELAY_AL14_WORKER_DRAIN unset) — Session ${sessionId} will route tickets and ` +
        `mark killed with reason "al-14-pending".`
    );
  } else {
    console.log(
      `[autonomous-loop] AL-4 steady-state driver engaged — ` +
        `Session ${sessionId} will drain the channel's ticket board until all tickets ` +
        `reach a terminal state, the 85% budget threshold winds down the loop, or a ` +
        `kill signal fires. worker drain enabled; tickets that reach awaiting-merge ` +
        `state will leave worktrees on disk until AL-5/PR-watcher wires merge-detection cleanup.`
    );
  }

  // AL-13/16: single channel store instance shared by the router, the
  // runner fleet, and the coordinator's decisions audit. Reusing one
  // instance avoids redundant on-disk state checks and keeps all
  // writes going through the same in-memory mirror.
  const channelStore = testOverrides?.channelStore ?? new ChannelStore();

  // AL-16: inter-admin coordination bus. Construct BEFORE the pool so
  // the pool can thread the reference into each session it spawns —
  // any in-process MCP handler built on a session's behalf then has
  // both coordinator and alias available at construction, which is
  // what `coordination_send` needs to route end-to-end. When the pool
  // is disabled (AL-13 pending path) the coordinator stays null; no
  // coordination is possible without admins to address anyway.
  //
  // The coordinator wraps a `pool` reference for alias-lookup on send;
  // we forward-declare a lazily-populated proxy that the pool writes
  // into after construction, so the ordering (coordinator-before-pool
  // for wiring, pool-before-use for routing) holds without a circular
  // constructor call. The proxy is a thin Pick<> view — the coordinator
  // only calls `getSession` / `listSessions` on it.
  let poolRef: RepoAdminPool | null = null;
  const poolView: Pick<RepoAdminPool, "getSession" | "listSessions"> = {
    getSession(alias: string) {
      return poolRef?.getSession(alias) ?? null;
    },
    listSessions() {
      return poolRef?.listSessions() ?? [];
    },
  };
  const coordinator = poolEnabled
    ? new Coordinator({
        pool: poolView,
        channelStore,
        channelId: channel.channelId,
      })
    : null;

  const pool = poolEnabled
    ? new RepoAdminPool({
        channel,
        allowedAliases: allowedRepos.map((r) => r.alias),
        fullAccess: channel.fullAccess ?? false,
        lifecycle,
        spawner: testOverrides?.repoAdminSpawner,
        rootDir: testOverrides?.rootDir,
        // AL-16: hand the shared bus to the pool so every session it
        // constructs receives the reference and can pass it into its
        // MCP handler at construction time.
        coordinator,
      })
    : null;
  poolRef = pool;

  if (pool) {
    try {
      await pool.start();
    } catch (err) {
      console.warn(
        `[autonomous-loop] repo-admin pool failed to boot: ${
          err instanceof Error ? err.message : String(err)
        }`
      );
    }
  }

  // AL-7 integration anchor. When AL-5 (PR reviewer) and AL-6 (audit agent)
  // land their driver hooks, each of their ack-requiring outputs must be
  // threaded through `decide({trust: opts.trust, ...})` from
  // `src/approvals/trust-gate.ts`. Under `supervised` the decision writes
  // a pending record to `~/.relay/approvals/<sessionId>/queue.jsonl`;
  // under `god` with `RELAY_AL7_GOD_AUTOMERGE=1` it writes an auto-approved
  // record (tagged `autoApprovedBy: "god-mode"`) AND returns
  // `{kind: "execute", auditRecordId, record}` so the caller performs the
  // merge / ticket write directly while the audit trail is already on
  // disk. AL-7 itself only produces + consumes the queue file; AL-8 owns
  // the approve / reject CLI surface.
  //
  // Concrete instantiation hint for AL-5 / AL-6 implementers — construct
  // the queue once at the start of the driver loop and thread the same
  // instance into every `decide()` call:
  //
  //   import { ApprovalsQueue, decide } from "../approvals/index.js";
  //   import { getRelayDir } from "../cli/paths.js";
  //
  //   const approvalsQueue = opts.approvalsQueue
  //     ?? new ApprovalsQueue({ rootDir: getRelayDir() });
  //
  //   // at PR-review-complete:
  //   const outcome = await decide({
  //     sessionId,
  //     trust: opts.trust,
  //     queue: approvalsQueue,
  //     action: { kind: "merge-pr", payload: { prUrl, reviewSummary } },
  //   });
  //   if (outcome.kind === "execute") { /* perform the merge */ }
  //
  // The `opts.approvalsQueue` fallback exists so the AL-4 driver test can
  // inject a tmp-dir-backed queue without touching `~/.relay/`.
  //
  // TODO(AL-5): at PR-review-complete, call `decide({..., action: {kind:
  //   "merge-pr", payload: {prUrl, reviewSummary}}})` and branch on the
  //   return. Do NOT auto-merge under supervised.
  // TODO(AL-6): at audit-proposal time, call `decide({..., action: {kind:
  //   "create-ticket", payload: {title, body, channelId}}})` and branch.
  //   Do NOT auto-write the ticket under supervised.
  //
  // AL-9: one pre-routing poll. Catches the "operator dropped STOP
  // before the loop reached its first tick" case — if we went straight
  // into routing without checking, a session could queue work into the
  // pool even though the user had already hit the kill switch. The
  // per-ticket check below handles STOP arriving mid-routing.
  await pollForStopAndWindDown();

  // Dispatch the work.
  //
  //  - No pool: single-pass stub path (legacy AL-13-pending behaviour).
  //  - Pool on, drain off: AL-13 routing single-pass (legacy al-14-pending).
  //  - Pool + drain on: AL-4 steady-state driver.
  //
  // Each branch returns its own terminal lifecycle target (state + reason).
  // The actual transition is applied once after the branch, so teardown
  // logic (coordinator.close → pool.stop → lifecycle.close → tracker.close)
  // runs uniformly regardless of how the driver exited.
  //
  // AL-9 background STOP poll armed by the pool-on branches so a STOP that
  // lands AFTER routing but BEFORE drain still flips the lifecycle into
  // `winding_down`. Runners are NEVER force-killed by the poll — the
  // spec says respect in-flight workers; only the lifecycle state is
  // flipped so downstream audit gating sees the right state.
  let terminalState: "killed" | "done";
  let terminalReason: DriverTerminalReason;
  let runners: TicketRunner[] = [];
  let stopPollTimer: NodeJS.Timeout | null = null;
  const armStopPoll = (): void => {
    const tick = async (): Promise<void> => {
      await pollForStopAndWindDown();
      if (!userStopped) {
        stopPollTimer = setTimeout(tick, stopPollIntervalMs);
        const h = stopPollTimer as { unref?: () => void };
        if (h && typeof h.unref === "function") h.unref();
      }
    };
    stopPollTimer = setTimeout(tick, stopPollIntervalMs);
    const h = stopPollTimer as { unref?: () => void };
    if (h && typeof h.unref === "function") h.unref();
  };

  if (!pool) {
    terminalState = "killed";
    terminalReason = "al-13-pending";
  } else if (!drainEnabled) {
    terminalState = "killed";
    terminalReason = "al-14-pending";
    try {
      const router = new TicketRouter({ pool, channel, channelStore });
      const boardTickets = await channelStore.readChannelTickets(channel.channelId);
      for (const ticket of boardTickets) {
        if (ticket.status !== "ready") continue;
        if (ticket.source === "linear") continue; // Linear mirror is read-only.
        // AL-9: re-poll before each ticket so a STOP that lands
        // mid-routing halts further dispatch. The already-routed
        // tickets continue through the drain phase — that's the
        // "respect in-flight workers" half of the graceful
        // wind-down contract.
        if (await pollForStopAndWindDown()) break;
        try {
          const result = await router.route(ticket);
          if (result.kind === "unroutable") {
            console.warn(
              `[autonomous-loop] ticket ${ticket.ticketId} unroutable (${result.reason}); ` +
                `surfaced to channel board as blocked.`
            );
          }
        } catch (err) {
          console.warn(
            `[autonomous-loop] router threw on ticket ${ticket.ticketId}: ${
              err instanceof Error ? err.message : String(err)
            }`
          );
        }
      }
    } catch (err) {
      console.warn(
        `[autonomous-loop] ticket routing pass failed: ${
          err instanceof Error ? err.message : String(err)
        }`
      );
    }
  } else {
    // AL-4: full steady-state driver. Own the runners' lifetime here so
    // the finally block below can signal them after any exit path.
    //
    // AL-9: arm the background STOP-file poll so a STOP landing during
    // the driver's steady-state loop still flips the lifecycle into
    // `winding_down`. Runners are NOT force-killed; the driver observes
    // the lifecycle transition via its `onTransition` subscription and
    // stops dispatching new tickets while letting in-flight workers
    // finish.
    armStopPoll();
    const result = await runSteadyStateDriver({
      pool,
      channel,
      channelStore,
      allowedRepos,
      lifecycle,
      workerSpawner: testOverrides?.workerSpawner ?? new WorkerSpawner(),
      pollIntervalMs: testOverrides?.pollIntervalMs ?? DEFAULT_POLL_INTERVAL_MS,
      prPoller: testOverrides?.prPoller,
    });
    terminalState = result.state;
    terminalReason = result.reason;
    runners = result.runners;
  }

  // AL-9: clear the background STOP poll before we tear down the runners.
  // A pending tick that hasn't fired yet is dropped — we're already on the
  // teardown path, a late lifecycle transition from the poll would race
  // against the explicit `killed`/`done` transitions below and throw.
  if (stopPollTimer !== null) {
    clearTimeout(stopPollTimer);
    stopPollTimer = null;
  }

  // Ensure any in-flight worker is signalled before we unwind. Safe to call
  // even if the runner's drain already finished — `stop()` is idempotent
  // and only targets still-running workers. Kept in a try/finally-style
  // position here (not inside the driver) so the pool-disabled and
  // routing-only branches also benefit if a future refactor makes them
  // construct runners.
  await Promise.all(
    runners.map((r) =>
      r.stop("autonomous-loop-exit").catch((err) => {
        console.warn(
          `[autonomous-loop] runner stop failed: ${
            err instanceof Error ? err.message : String(err)
          }`
        );
      })
    )
  );
  // Drain any tail of per-ticket writes (markTicketFailed /
  // updateTicket / feed posts) that the runner's `runOneTicket` might
  // still be finishing after its worker exited via `stop()`. Calling
  // `drain()` on a stopped runner returns the memoized in-flight
  // promise (if any) or resolves immediately — either way, we block
  // until the channel-store writes finish so teardown + tmp-dir
  // cleanup don't race with them.
  await Promise.all(
    runners.map((r) =>
      r.drain().catch((err) => {
        console.warn(
          `[autonomous-loop] runner tail drain failed: ${
            err instanceof Error ? err.message : String(err)
          }`
        );
      })
    )
  );

  // AL-14 follow-up: terminal worktree sweep. Before we transition the
  // lifecycle, walk any ticket in `verifying` state whose PR has already
  // merged and destroy the worktree. The driver's steady-state merge
  // subscription (above) covers mid-run merges; this sweep covers the
  // race where a PR merged AFTER the driver exited its loop but BEFORE
  // the terminal transition fired. `olderThanHours: 0` so a freshly-
  // merged ticket isn't skipped by the 24h grace window — the grace
  // window exists for the cross-session CLI crash-recovery case, not
  // for a driver that just observed the merge itself.
  if (pool && drainEnabled) {
    try {
      await sweepAbandonedWorktrees({
        channelStore,
        spawner: testOverrides?.workerSpawner ?? new WorkerSpawner(),
        channelId: channel.channelId,
        ghPrView: testOverrides?.ghPrView,
        olderThanHours: 0,
        dryRun: false,
        rootDir: testOverrides?.rootDir,
      });
    } catch (err) {
      console.warn(
        `[autonomous-loop] terminal worktree sweep failed: ${
          err instanceof Error ? err.message : String(err)
        }`
      );
    }
  }

  // AL-6: post-completion audit hook. Fires exactly once per session, in
  // the window between "last ticket drained" and "terminal lifecycle
  // transition". Gated internally on budget headroom + ledger health
  // (see `runPostCompletionAudit`). The `onAllTicketsComplete` seam is
  // exposed so AL-4's steady-state driver — which knows about retries,
  // stop-on-failure flags, etc. — can decide when "all tickets done"
  // truly holds.
  //
  // Only fires when the worker drain was enabled. In drain-disabled mode
  // no tickets actually ran (AL-13 pending), so auditing what "just
  // completed" is meaningless and would write stale proposals from a
  // stale board. We ALSO suppress the auto-audit on a user-stop: the
  // AL-9 audit gate below handles the "killed but ledger green" path,
  // and running both would double-log.
  if (pool && drainEnabled && !userStopped && terminalReason !== "user-stop-signal") {
    const headroomPct = Math.max(0, 100 - tracker.pct);
    const auditCtx: AllTicketsCompleteContext = {
      channel,
      channelStore,
      sessionId,
      tracker,
      budgetHeadroomPct: headroomPct,
      auditInvoker: testOverrides?.auditInvoker,
    };
    try {
      if (opts.onAllTicketsComplete) {
        await opts.onAllTicketsComplete(auditCtx);
      } else {
        await defaultOnAllTicketsComplete(auditCtx);
      }
    } catch (err) {
      // A misbehaving hook must NOT keep the loop from tearing down
      // its pool / lifecycle / tracker. Warn and continue.
      console.warn(
        `[autonomous-loop] onAllTicketsComplete hook failed: ${
          err instanceof Error ? err.message : String(err)
        }`
      );
    }
  }

  // Only transition if we're still in a non-terminal state. The AL-2
  // watchdog or an external signal may have already pushed us to
  // `killed`; respect that and skip the transition in that case to avoid
  // throwing a LifecycleTransitionError the caller would have to catch.
  //
  // AL-9: when `userStopped` is set (or the driver's own wind-down
  // observation ran with `user-stop-signal`), we treat the terminal as
  // a kill and stamp STOP_FILE_REASON so log consumers + the audit gate
  // can distinguish a user-initiated stop from a natural terminal.
  // The driver's reason mirrors the lifecycle's `winding_down` event,
  // so a STOP that fired after the caller's pre-routing poll still
  // surfaces here even if the caller's `userStopped` flag didn't flip
  // (e.g. a background-poll race where the driver's sub saw the
  // transition first).
  const driverSawStop = terminalReason === "user-stop-signal";
  const effectiveUserStopped = userStopped || driverSawStop;
  const currentState = lifecycle.state;
  const effectiveState: "killed" | "done" = effectiveUserStopped ? "killed" : terminalState;
  const effectiveReason = effectiveUserStopped ? STOP_FILE_REASON : terminalReason;
  try {
    if (currentState !== "killed" && currentState !== "done") {
      try {
        // The success path needs two transitions (dispatching →
        // winding_down → done) because the state machine forbids the
        // direct dispatching → done edge. Keeping this as two separate
        // awaits also leaves a clean hook point for AL-6's audit pass to
        // slot `winding_down → audit → done` in without touching the
        // happy-path callsite.
        if (effectiveState === "done") {
          if (currentState === "dispatching") {
            await lifecycle.transition("winding_down", effectiveReason);
          }
          // After winding_down we must land on `done` — but only if
          // nothing raced us into `killed` between transitions.
          if (lifecycle.state === "winding_down") {
            await lifecycle.transition("done", effectiveReason);
          }
        } else {
          await lifecycle.transition("killed", effectiveReason);
        }
      } catch (err) {
        console.warn(
          `[autonomous-loop] failed to transition lifecycle to ${effectiveState}: ${
            err instanceof Error ? err.message : String(err)
          }`
        );
      }
    }
  } finally {
    // AL-9: clear the STOP file after terminal transition so a subsequent
    // `rly run --autonomous` against the same sessionId doesn't immediately
    // kill from a leftover STOP file. Defensive — swallow errors since a
    // leftover STOP file is a minor correctness issue, never a reason to
    // fail teardown.
    try {
      await clearStopFile(sessionId, testOverrides?.rootDir);
    } catch (err) {
      console.warn(
        `[autonomous-loop] clearStopFile failed for session ${sessionId}: ${
          err instanceof Error ? err.message : String(err)
        }`
      );
    }
  }

  // AL-9: post-completion audit gate. When the session is terminating
  // cleanly (or via user-stop after a fully-green routing pass), we
  // should run the AL-6 audit agent. The spec: "Killed session still
  // runs post-completion audit IF ledger was all-green before kill;
  // otherwise skips audit."
  //
  // AL-6 is not yet merged into this branch, so the implementation
  // here is intentionally a no-op with a structured log line. When
  // AL-6 lands, the `runAudit()` call slots in where the log lives —
  // the gating logic (ledger inspection, user-stop tolerance) is the
  // piece AL-9 owns and ships now.
  try {
    await maybeRunAuditHook({
      sessionId,
      channel,
      channelStore,
      userStopped: effectiveUserStopped,
      lifecycle,
    });
  } catch (err) {
    console.warn(
      `[autonomous-loop] audit gate check failed: ${
        err instanceof Error ? err.message : String(err)
      }`
    );
  }

  // AL-16: close the coordinator BEFORE stopping the pool. Order
  // matters — once `pool.stop()` starts, each session unwinds and
  // `getSession(to)` begins returning null, so a late-arriving
  // `coordination_send` would resolve with `no-such-admin` (generic,
  // confusing post-mortem) instead of the cleaner `coordinator-closed`
  // the close path produces. Closing first guarantees the in-flight
  // race resolves on the close rail.
  if (coordinator) {
    try {
      await coordinator.close();
    } catch (err) {
      console.warn(
        `[autonomous-loop] coordinator close failed: ${
          err instanceof Error ? err.message : String(err)
        }`
      );
    }
  }

  // Terminal lifecycle transition triggers the pool's auto-stop, but also
  // await it here so teardown happens before the CLI returns.
  if (pool) {
    try {
      await pool.stop();
    } catch (err) {
      console.warn(
        `[autonomous-loop] repo-admin pool stop failed: ${
          err instanceof Error ? err.message : String(err)
        }`
      );
    }
  }

  // Close out resources. Any in-flight writes drain here; errors are
  // warned but never thrown — the CLI exit path is the wrong place to
  // surface a teardown failure.
  try {
    await lifecycle.close();
  } catch (err) {
    console.warn(
      `[autonomous-loop] lifecycle close failed: ${
        err instanceof Error ? err.message : String(err)
      }`
    );
  }
  try {
    await tracker.close();
  } catch (err) {
    console.warn(
      `[autonomous-loop] tracker close failed: ${err instanceof Error ? err.message : String(err)}`
    );
  }
}

/**
 * AL-6: default implementation of the `onAllTicketsComplete` seam.
 * Reads the channel's current ticket ledger + decisions, then hands them
 * to {@link runPostCompletionAudit}. Logs the outcome so an operator can
 * tell from the autonomous-loop's final output whether the audit fired,
 * was gated off, or produced an invalid response.
 *
 * Auto-audit is disabled when no `auditInvoker` is supplied — i.e. the
 * production default until AL-4 wires the agent factory into this seam.
 * Tests explicitly pass an invoker via `testOverrides.auditInvoker`.
 */
async function defaultOnAllTicketsComplete(ctx: AllTicketsCompleteContext): Promise<void> {
  if (!ctx.auditInvoker) {
    // No invoker, no audit. Keeping this silent by default — the
    // autonomous-loop's pre-audit log already told the operator that
    // AL-4 is not yet wired; shouting "audit skipped (no invoker)" on
    // every successful run would just add noise.
    return;
  }

  const tickets = await ctx.channelStore.readChannelTickets(ctx.channel.channelId);
  const decisions = await ctx.channelStore.listDecisions(ctx.channel.channelId);

  const result: AuditRunResult = await runPostCompletionAudit({
    channel: ctx.channel,
    channelStore: ctx.channelStore,
    run: {
      sessionId: ctx.sessionId,
      tickets,
      decisions,
      // Recent git log capture is deferred to AL-4's richer driver,
      // which knows which repos the run touched. The audit prompt
      // tolerates an empty array, so skipping here produces no noise.
      recentCommits: [],
    },
    budgetHeadroomPct: ctx.budgetHeadroomPct,
    invokeAudit: ctx.auditInvoker,
  });

  if (result.kind === "fired") {
    console.log(
      `[autonomous-loop] audit-agent fired — ${result.proposalsWritten} proposal(s) written.`
    );
  } else if (result.kind === "skipped") {
    console.log(`[autonomous-loop] audit-agent skipped (${result.reason}).`);
  } else {
    console.warn(
      `[autonomous-loop] audit-agent returned invalid response (${result.issues.length} issue(s)).`
    );
  }
}

/**
 * AL-9's audit gate. AL-6 ships the actual audit agent; this helper
 * owns the gating logic and logs the decision so the AL-6 merge is a
 * drop-in call replacement (swap the `console.log` for the real
 * runAudit call). Separating gate from actuator keeps both tickets
 * independently verifiable.
 *
 * ## Gate
 *
 * The spec (AL-9 AC3): "Killed session still runs the post-completion
 * audit IF the ledger was all-green before the kill; otherwise skips
 * audit." The interpretation:
 *
 *   - "all-green" = every ticket on the channel's board ended in
 *     `completed` (or was never picked up, i.e. still `pending` /
 *     `blocked` — those aren't failures, just unreached work).
 *   - Any `failed` / `retry` / `executing` / `verifying` ticket means
 *     the ledger wasn't green; skip audit because auditing a partial
 *     run would produce a misleading report.
 *
 * Natural terminal (no user stop, no threshold kill) follows the same
 * rule — if the routing pass finished cleanly and every ticket is
 * completed, audit; otherwise skip.
 */
async function maybeRunAuditHook(args: {
  sessionId: string;
  channel: Channel;
  channelStore: ChannelStore;
  userStopped: boolean;
  lifecycle: SessionLifecycle;
}): Promise<void> {
  const { sessionId, channel, channelStore, userStopped } = args;

  const tickets = await channelStore.readChannelTickets(channel.channelId);
  // "green" means: no ticket ended in an unhealthy terminal / partial
  // state. A completely empty board is vacuously green (the audit on
  // an empty session is trivially "nothing to report"); AL-6 decides
  // whether to short-circuit in that case.
  const unhealthy = tickets.filter(
    (t) =>
      t.status === "failed" ||
      t.status === "retry" ||
      t.status === "executing" ||
      t.status === "verifying"
  );
  const allGreen = unhealthy.length === 0;

  if (!allGreen) {
    console.log(
      `[autonomous-loop] skipping post-completion audit for session ${sessionId} — ` +
        `ledger has ${unhealthy.length} non-green ticket(s) ` +
        `(${unhealthy
          .slice(0, 3)
          .map((t) => `${t.ticketId}:${t.status}`)
          .join(", ")}${unhealthy.length > 3 ? ", …" : ""}).`
    );
    return;
  }

  // Green — audit would run. AL-6 slots its runAudit() here. The log
  // line is the marker the audit gate fires; AL-9's tests key on it.
  const trigger = userStopped ? "user-stop-signal" : "natural-terminal";
  console.log(
    `[autonomous-loop] post-completion audit eligible for session ${sessionId} ` +
      `(trigger=${trigger}, ticketsGreen=${tickets.length}); AL-6 hook not yet wired.`
  );
}

/**
 * AL-4 steady-state driver internals. Split out of
 * {@link startAutonomousSession} so the branching in the public entrypoint
 * stays readable — the driver is itself non-trivial (poll-route-drain loop
 * with lifecycle observation + winding-down semantics) and benefits from a
 * dedicated scope for its per-driver state.
 */
interface SteadyStateDriverArgs {
  pool: RepoAdminPool;
  channel: Channel;
  channelStore: ChannelStore;
  allowedRepos: RepoAssignment[];
  lifecycle: SessionLifecycle;
  workerSpawner: WorkerSpawner;
  pollIntervalMs: number;
  /**
   * AL-14 follow-up: optional merge-event subscription bus. When set,
   * the driver registers an `onMerged` listener and routes each merge
   * event to the matching {@link TicketRunner.handlePrMerged} so the
   * ticket's worktree is destroyed within one tick of the merge.
   * Absent = no steady-state merge cleanup (the terminal sweep still
   * runs on driver exit).
   */
  prPoller?: Pick<PrPoller, "onMerged">;
}

interface SteadyStateDriverResult {
  state: "done" | "killed";
  reason: DriverTerminalReason;
  /**
   * Runners the driver constructed. Handed back so the caller can
   * `.stop()` them uniformly alongside runners from other dispatch
   * branches. Empty array when no admin was live at driver entry.
   */
  runners: TicketRunner[];
}

/**
 * Run the AL-4 steady-state loop against an already-booted pool.
 *
 * Loop body:
 *   1. Check for lifecycle transitions that should stop the driver
 *      (`winding_down` → stop dispatching; `killed` → abort cleanly).
 *   2. Read the channel's ticket board. Filter to `ready` tickets we
 *      haven't routed yet and that target an alias we have an admin for.
 *   3. Route each via {@link TicketRouter}. Unroutable tickets are
 *      surfaced to the feed by the router itself — the driver just logs
 *      the result.
 *   4. Ensure each live admin has a {@link TicketRunner} (lazily
 *      constructed; reused across cycles so repeated calls to `.drain()`
 *      coalesce on the runner's own `drainPromise` memo).
 *   5. Kick every runner's `.drain()`. These run in parallel across
 *      admins and serialize inside each admin — the AL-14 invariant.
 *   6. Wait for one of: (a) every runner's drain resolves — board either
 *      drained or new tickets need another pass; (b) lifecycle flips to
 *      a terminal / wind-down state.
 *   7. Decide whether to loop again or exit. Exit when the board has no
 *      more `ready` tickets AND every runner is idle. Otherwise sleep
 *      for `pollIntervalMs` (so we don't hot-spin when the board is
 *      static but a runner is still waiting on a worker exit) and loop.
 */
async function runSteadyStateDriver(args: SteadyStateDriverArgs): Promise<SteadyStateDriverResult> {
  const {
    pool,
    channel,
    channelStore,
    allowedRepos,
    lifecycle,
    workerSpawner,
    pollIntervalMs,
    prPoller,
  } = args;

  const router = new TicketRouter({ pool, channel, channelStore });

  /**
   * AL-14 follow-up: reverse index from ticketId back to the runner that
   * owns it. Populated when a runner fires `worker-pr-opened` (the earliest
   * point we know a worktree exists for that ticket) and consumed by the
   * pr-poller `onMerged` bridge below. We keep this as a flat map rather
   * than walking every runner's `listInflight()` on each event because
   * merge events can fire long after the drain loop has moved on — the
   * owning runner may no longer have the ticket in its in-flight map by
   * the time `handlePrMerged` is called. The runner's own idempotency
   * guard handles that case, but the lookup has to still find the
   * runner first.
   */
  const runnerByTicketId = new Map<string, TicketRunner>();

  /**
   * AL-14 follow-up: unsubscribe from the pr-poller's merge bus when the
   * driver returns. Held here (not inside the `try/finally` below) so
   * the detach happens even on early `break` paths.
   */
  let unsubscribePrPoller: (() => void) | null = null;
  if (prPoller) {
    unsubscribePrPoller = prPoller.onMerged((evt: PrMergedEvent) => {
      const runner = runnerByTicketId.get(evt.ticketId);
      if (!runner) {
        // Merge event for a ticket this driver doesn't own (e.g. a
        // ticket routed by a prior session, or a manual `pr-watch`
        // track). The terminal sweep catches this — we skip here so a
        // stale subscription doesn't log noise per tick.
        return;
      }
      // Kick off the merge handler but don't block the pr-poller's
      // tick — the handler does its own error logging.
      void runner.handlePrMerged(evt.ticketId).catch((err) => {
        console.warn(
          `[autonomous-loop] handlePrMerged(${evt.ticketId}) threw: ${
            err instanceof Error ? err.message : String(err)
          }`
        );
      });
    });
  }

  /**
   * Tickets we've already handed to the router this session. Prevents a
   * re-poll from double-routing a ticket that's still showing `ready` on
   * disk — the admin's queue de-dupes too, but keeping it local to the
   * driver saves a redundant pool+session call per cycle and makes the
   * routed-set observable for the "board drained" exit check.
   */
  const dispatched = new Set<string>();

  /**
   * Runners keyed by alias, lazily constructed as admins become live.
   * Reused across loop iterations — calling `.drain()` on an idle runner
   * starts a fresh drain; calling on an in-flight runner returns the
   * memoized promise (see `TicketRunner.drain()`).
   *
   * When the pool restarts an admin (same alias, new session), the old
   * runner's `admin` reference points at the dead session. We track the
   * admin session id alongside the runner so the rebuild-on-pool-restart
   * branch below can detect the mismatch and swap in a fresh runner.
   */
  const runnersByAlias = new Map<string, TicketRunner>();
  const runnerAdminSessionByAlias = new Map<string, string>();
  const allowedAliases = new Set(allowedRepos.map((r) => r.alias));

  /**
   * Latch flipped when the lifecycle moves out of `dispatching`. Callers
   * read this to short-circuit new ticket routing while still letting
   * in-flight drains finish (the "stop dispatching new, let workers
   * complete" semantic the AL-4 spec calls out).
   */
  let acceptingNew = true;
  /**
   * Latch flipped when the lifecycle reaches `killed`. Pushes the driver
   * into the abort path: stop dispatching, signal in-flight runners, do
   * NOT transition back to `done` at exit.
   */
  let killed = false;
  /**
   * Captured kill reason for the terminal transition. Mirrors what the
   * lifecycle recorded so the driver surfaces the SAME reason code on
   * exit (e.g. `wall-clock-exceeded`, `token-budget-95pct-hard-stop`).
   */
  let killReason: DriverTerminalReason = "killed-externally";
  /**
   * Captured wind-down reason. Same rationale as `killReason` — we want
   * the terminal `done` transition to carry the reason code from the
   * lifecycle's own `dispatching → winding_down` transition.
   */
  let windDownReason: DriverTerminalReason = "budget-winding-down";

  // AL-9: the STOP-file pre-routing poll in startAutonomousSession may
  // have already transitioned the lifecycle to `winding_down` (or
  // `killed`, from the `planning` short-circuit) BEFORE the driver
  // subscribed to the lifecycle event stream. Read the initial state
  // here so we honor that prior transition instead of accepting new
  // tickets as if the stop never happened.
  const initialState = lifecycle.state;
  if (initialState === "winding_down") {
    acceptingNew = false;
    windDownReason = "user-stop-signal";
  } else if (initialState === "killed" || initialState === "done") {
    acceptingNew = false;
    killed = initialState === "killed";
    killReason = "user-stop-signal";
  }

  /**
   * Waker pattern: one pending promise that resolves whenever the
   * lifecycle transitions to something the driver cares about. Used to
   * race against runner drain + poll sleep so we exit the wait fast
   * instead of waiting out a full `pollIntervalMs` after a kill fires.
   */
  let wakerResolve: (() => void) | null = null;
  let wakerPromise = new Promise<void>((resolve) => {
    wakerResolve = resolve;
  });
  const wake = () => {
    const r = wakerResolve;
    wakerResolve = null;
    wakerPromise = new Promise<void>((resolve) => {
      wakerResolve = resolve;
    });
    if (r) r();
  };

  const unsubscribeLifecycle = lifecycle.onTransition((evt: TransitionEvent) => {
    // The lifecycle state machine fires one event per successful
    // transition. Only the transitions we act on matter; other edges
    // (e.g. `planning → dispatching` that predates our subscription)
    // aren't routed here at all.
    if (evt.to === "winding_down") {
      acceptingNew = false;
      // Preserve the original reason when the budget-trigger fired the
      // transition itself; fall back to `budget-winding-down` when the
      // lifecycle didn't stamp one (the only current path that lacks a
      // reason is a test-driven manual transition).
      windDownReason = "budget-winding-down";
      if (evt.reason) {
        // Reason codes from the lifecycle are free-form strings; pin the
        // common ones to the driver's narrower union for consistent exit
        // stamping. Unknown values fall back to `budget-winding-down`
        // since `winding_down` today is only reached via the budget
        // threshold bus or the AL-9 STOP-file poll.
        if (evt.reason === STOP_FILE_REASON) {
          windDownReason = "user-stop-signal";
        } else if (
          evt.reason === "token-budget-85pct" ||
          evt.reason === "budget-winding-down" ||
          evt.reason === "done"
        ) {
          windDownReason = "budget-winding-down";
        }
      }
      // Tell each runner to stop pulling new tickets off the admin's
      // pending queue. Workers already in flight run to completion —
      // this is the "let in-flight workers complete, no new
      // dispatches" semantic in the AL-4 spec. Runners constructed
      // after this point also inherit the flag via the explicit call
      // on `ensureRunner`.
      for (const runner of runnersByAlias.values()) runner.stopAcceptingNew();
      wake();
    } else if (evt.to === "killed") {
      acceptingNew = false;
      killed = true;
      if (evt.reason === "wall-clock-exceeded") {
        killReason = "wall-clock-exceeded";
      } else if (evt.reason === STOP_FILE_REASON) {
        killReason = "user-stop-signal";
      } else {
        killReason = "killed-externally";
      }
      // Same as the wind-down path: stop pulling new tickets. The
      // top-level cleanup then calls `runner.stop("autonomous-loop-
      // exit")` which signals the in-flight workers — no double-drain
      // between here and there.
      for (const runner of runnersByAlias.values()) runner.stopAcceptingNew();
      wake();
    }
  });

  try {
    while (true) {
      if (killed) break;

      // Read-route phase. Skipped when the lifecycle has stopped
      // accepting new tickets — in-flight drains still run below, we
      // just don't add to their queues.
      if (acceptingNew) {
        let boardTickets: TicketLedgerEntry[];
        try {
          boardTickets = await channelStore.readChannelTickets(channel.channelId);
        } catch (err) {
          console.warn(
            `[autonomous-loop] failed to read channel tickets: ${
              err instanceof Error ? err.message : String(err)
            }`
          );
          boardTickets = [];
        }

        for (const ticket of boardTickets) {
          if (killed || !acceptingNew) break;
          if (ticket.status !== "ready") continue;
          if (ticket.source === "linear") continue;
          if (dispatched.has(ticket.ticketId)) continue;
          // Filter to allowed repos at dispatch time — the CLI's
          // `--allow-repo` flag subsets `allowedRepos` from the full
          // channel assignment list, and tickets targeting an excluded
          // alias must be left on the board for a later (differently-
          // scoped) session to pick up rather than force-routed to a
          // fallback admin.
          if (ticket.assignedAlias && !allowedAliases.has(ticket.assignedAlias)) {
            continue;
          }
          try {
            const result = await router.route(ticket);
            // Regardless of routed/unroutable, record so we don't
            // reprocess next cycle. The router writes the blocked
            // status to disk on `unroutable` so the next board read
            // already reflects the new status — but the local set
            // guards against racing with that write.
            dispatched.add(ticket.ticketId);
            if (result.kind === "unroutable") {
              console.warn(
                `[autonomous-loop] ticket ${ticket.ticketId} unroutable (${result.reason}); ` +
                  `surfaced to channel board as blocked.`
              );
            }
          } catch (err) {
            console.warn(
              `[autonomous-loop] router threw on ticket ${ticket.ticketId}: ${
                err instanceof Error ? err.message : String(err)
              }`
            );
            // Mark it dispatched even on throw so we don't hot-loop
            // retrying a broken ticket every cycle. Operator inspection
            // is the recovery path.
            dispatched.add(ticket.ticketId);
          }
        }
      }

      // Ensure a runner exists for each live admin. Do this every cycle
      // because the pool may restart a session between iterations, and a
      // fresh `ready` admin that didn't exist on the last pass deserves
      // its queue drained on this one.
      const liveAdmins = pool.listSessions().filter((s: RepoAdminSession) => s.state === "ready");
      for (const admin of liveAdmins) {
        const existing = runnersByAlias.get(admin.alias);
        const trackedSession = runnerAdminSessionByAlias.get(admin.alias);
        // Fresh admin (first time) OR pool restart (alias alive but
        // backed by a new child session) both need a new runner. In the
        // restart case the old runner's `admin` reference is a dead
        // session — its `takeNextPendingTicket` would keep returning the
        // ghost session's stale queue, and any new enqueues the pool
        // routes to the NEW session would never be picked up. Detect by
        // comparing the admin's current sessionId against the tracked
        // one; stale means rebuild.
        if (existing && trackedSession === admin.sessionId) continue;
        const assignment = channel.repoAssignments?.find((a) => a.alias === admin.alias);
        if (!assignment) continue;
        const runner = new TicketRunner({
          admin,
          repoAssignment: assignment,
          channel,
          channelStore,
          spawner: workerSpawner,
        });
        // A runner created AFTER a wind-down / kill fired inherits the
        // stop-accepting-new flag immediately so its drain loop exits
        // on the first iteration without pulling anything off the
        // admin's queue. Without this, a late-booting admin (pool
        // restart during wind-down) could spawn workers after we
        // promised not to.
        if (!acceptingNew) runner.stopAcceptingNew();
        runnersByAlias.set(admin.alias, runner);
        runnerAdminSessionByAlias.set(admin.alias, admin.sessionId);

        // AL-14 follow-up: register this runner as the owner of any
        // ticket it spawns a worker for, so a later pr-poller merge
        // event can route back to it. We subscribe to the runner's
        // `worker-pr-opened` event (the earliest point where a worktree
        // exists + the PR URL is known) rather than `worker-started`
        // because tickets that fail without opening a PR will never
        // receive a merge event.
        runner.on("worker-pr-opened", (evt) => {
          runnerByTicketId.set(evt.ticketId, runner);
        });
        // Drop the map entry when the runner itself finalises the
        // merge so a late event (e.g. a duplicate poll hit) doesn't
        // fire `handlePrMerged` twice.
        runner.on("ticket-merged", (evt) => {
          runnerByTicketId.delete(evt.ticketId);
        });
      }

      // Kick + await drain. Runners serialize internally, parallelize
      // across admins. `drain()` is idempotent — calling on an in-flight
      // runner returns the memoized promise — so we can kick every cycle
      // without fear of double-processing.
      const drainPromises = Array.from(runnersByAlias.values()).map((r) =>
        r.drain().catch((err) => {
          console.warn(
            `[autonomous-loop] ticket runner drain failed: ${
              err instanceof Error ? err.message : String(err)
            }`
          );
        })
      );

      // Race drain completion against lifecycle wake. If a kill or
      // wind-down fires mid-drain, we want to exit the wait and hit the
      // top-of-loop checks without waiting out the full drain (for
      // kills) or polling once more (for wind-downs).
      await Promise.race([Promise.all(drainPromises), wakerPromise]);

      if (killed) break;

      // Did the drain actually finish? If not (a waker fired first), we
      // still want to wait for it before deciding whether to exit,
      // because a drain in-flight means the board isn't drained.
      await Promise.all(drainPromises);

      // Exit decision:
      //  - If we're winding down (acceptingNew=false) and all drains
      //    are settled, we're done — no new tickets will be dispatched
      //    and all in-flight finished above.
      //  - Otherwise, check whether the board has any `ready` tickets
      //    left we haven't already dispatched. If not, and no runner is
      //    waiting on a worker, the board is drained — exit `done`.
      //  - If there's work left (newly-ready tickets from a completed
      //    dependency, or a runner still has a queued ticket), loop
      //    after a short poll.
      if (!acceptingNew) {
        // Winding down: drains finished above, nothing else to do.
        break;
      }

      // Look for remaining work the driver can still act on.
      let moreWork = false;
      try {
        const board = await channelStore.readChannelTickets(channel.channelId);
        for (const ticket of board) {
          if (ticket.status !== "ready") continue;
          if (ticket.source === "linear") continue;
          if (dispatched.has(ticket.ticketId)) continue;
          if (ticket.assignedAlias && !allowedAliases.has(ticket.assignedAlias)) continue;
          moreWork = true;
          break;
        }
      } catch (err) {
        // A transient read failure shouldn't exit the loop — warn and
        // try again on the next cycle. If it's persistent, the surface
        // eventually becomes an operator-visible log volume.
        console.warn(
          `[autonomous-loop] failed to re-read channel tickets for exit check: ${
            err instanceof Error ? err.message : String(err)
          }`
        );
        moreWork = true;
      }

      if (!moreWork) {
        // Also confirm no runner is still sitting on an in-flight
        // worker. The drain `.listInflight()` gives the current snapshot;
        // `running` means a worker is still executing (hasn't hit
        // `awaiting-merge` / `failed` / `completed` yet), so we wait.
        let runnerBusy = false;
        for (const runner of runnersByAlias.values()) {
          const active = runner.listInflight().some((t) => t.state === "running");
          if (active) {
            runnerBusy = true;
            break;
          }
        }
        if (!runnerBusy) {
          break;
        }
      }

      // Poll sleep — racing against the waker so kill/wind-down exits
      // promptly without waiting out the full interval.
      await Promise.race([sleep(pollIntervalMs), wakerPromise]);
    }
  } finally {
    unsubscribeLifecycle();
    if (unsubscribePrPoller) unsubscribePrPoller();
  }

  const runners = Array.from(runnersByAlias.values());
  if (killed) {
    return { state: "killed", reason: killReason, runners };
  }
  if (!acceptingNew) {
    return { state: "done", reason: windDownReason, runners };
  }
  return { state: "done", reason: "done", runners };
}

function sleep(ms: number): Promise<void> {
  if (ms <= 0) return Promise.resolve();
  return new Promise((resolve) => {
    const handle = setTimeout(resolve, ms);
    // Don't keep the event loop alive if the host process is otherwise
    // idle. The driver always races the sleep against other awaits so
    // an unref'd timer is harmless.
    if (typeof (handle as { unref?: () => void }).unref === "function") {
      (handle as { unref: () => void }).unref();
    }
  });
}

/**
 * Env var gating AL-14's worker-drain phase. Distinct from the pool-enabled
 * flag so an operator can flip the pool on (AL-12 boot/restart/shutdown
 * exercise) without automatically enabling worker spawning + drain. The
 * split exists because AL-4's merge-detection cleanup has not shipped yet:
 * every worker that reaches `awaiting-merge` state leaves its worktree on
 * disk until AL-4 lands, and we want operators to opt into that leak
 * deliberately. Default **off**.
 *
 * Recognised true-ish values: `"1"`, `"true"`, `"yes"`, `"on"`
 * (case-insensitive). Anything else — including typos — is treated as off
 * so a misconfigured flag doesn't silently leak worktrees.
 */
export const RELAY_AL14_WORKER_DRAIN = "RELAY_AL14_WORKER_DRAIN";

/**
 * Parse {@link RELAY_AL14_WORKER_DRAIN} into a bool. See the constant's
 * docstring for the "why two flags" rationale.
 */
export function isWorkerDrainEnabled(env: NodeJS.ProcessEnv = process.env): boolean {
  const raw = env[RELAY_AL14_WORKER_DRAIN];
  if (!raw) return false;
  const normalized = raw.trim().toLowerCase();
  return normalized === "1" || normalized === "true" || normalized === "yes" || normalized === "on";
}

/**
 * Options handed to {@link startAutonomousSession} by the CLI entrypoint
 * (`rly run --autonomous`, AL-3). AL-3 constructs the session scaffolding —
 * lifecycle, token tracker, audit decision entry, allowed-repo filter — and
 * then delegates actual ticket dispatch + driver loop to the function below.
 *
 * AL-4 owns the real implementation. This file ships a stub that logs a
 * "driver not yet implemented" message, transitions the lifecycle to
 * `killed`, and returns cleanly so the CLI can exit 0. Anything the driver
 * needs at steady state — scheduler access, approval queue, kill-switch
 * polling, pr-review integration, audit agent — is AL-4's problem, not
 * AL-3's, and must NOT creep into the stub body.
 */
export interface StartAutonomousSessionOptions {
  /** Session id generated by the CLI; shared by tracker + lifecycle + audit. */
  sessionId: string;
  /** The channel whose ticket board this session executes against. */
  channel: Channel;
  /** Token-budget tracker. Caller keeps ownership; AL-4 records against it. */
  tracker: TokenTracker;
  /** Lifecycle state machine. Caller has already transitioned it into
   * `dispatching`; AL-4's driver owns subsequent transitions. */
  lifecycle: SessionLifecycle;
  /** Trust mode as selected on the CLI. The stub records it but never acts
   * on it. AL-5 / AL-7 gate the real behaviour difference between
   * `supervised` and `god`. */
  trust: "supervised" | "god";
  /** Repo assignments the session is allowed to dispatch work into. Either
   * the subset selected by `--allow-repo` or — when the flag is absent —
   * every assignment on the channel. AL-4 uses this as a filter at dispatch
   * time. */
  allowedRepos: RepoAssignment[];
  /**
   * **Test-only** injection hooks. Production callers MUST NOT set these —
   * they exist solely so the AL-14 drain integration test can exercise the
   * pool-boot + router + runner drain path end-to-end without a real
   * `claude` binary. Three seams are exposed:
   *
   *  - `channelStore`: replaces the default `new ChannelStore()` so tests
   *    can drive a tmp-dir-backed store with seeded channels + tickets.
   *  - `repoAdminSpawner`: threaded into the `RepoAdminPool` so its per-
   *    admin children are fakes (no real CLI).
   *  - `workerSpawner`: replaces `new WorkerSpawner()` so the runner's
   *    spawn + monitor loop fires deterministically against a fake.
   *
   * All three are optional and ALL must be `undefined` in production.
   * Presence of any field constitutes a test invocation.
   */
  testOverrides?: {
    channelStore?: ChannelStore;
    repoAdminSpawner?: RepoAdminProcessSpawner;
    workerSpawner?: WorkerSpawner;
    /**
     * Override the pool's `~/.relay` base dir so the test's tmp dir is
     * used instead of the real home dir for session/admin logs.
     */
    rootDir?: string;
    /**
     * AL-6: replace the audit agent's LLM invoker with a deterministic
     * scripted function. Tests use this to return pre-shaped JSON so the
     * gating + decision-write path runs end-to-end without a real CLI
     * spawn. Production leaves this unset; `runPostCompletionAudit` is
     * never reached in pre-AL-4 shapes (the driver stub skips it) so the
     * production default is "no invoker, no audit" until AL-4 wires in a
     * factory-built agent.
     */
    auditInvoker?: AuditInvoker;
  };
  /**
   * AL-6 seam: invoked exactly once when the driver reaches "all
   * tickets done" — after the last ticket finishes draining and before
   * the lifecycle transitions to its terminal state. AL-4's real driver
   * is expected to populate this; until AL-4 ships, the default
   * behaviour (defined below) is to directly invoke
   * {@link runPostCompletionAudit} against the channel's ledger. Callers
   * that want to suppress the audit entirely can pass a no-op.
   *
   * The hook runs synchronously inside the loop's post-drain block: a
   * throw here is caught and warned, never rethrown, because the
   * autonomous loop's shutdown path must be robust to third-party
   * failures.
   */
  onAllTicketsComplete?: (ctx: AllTicketsCompleteContext) => Promise<void>;
}

/**
 * Context handed to the {@link StartAutonomousSessionOptions.onAllTicketsComplete}
 * seam. Everything the seam needs to run AL-6's
 * {@link runPostCompletionAudit} is on this object; no "reach into the
 * loop's internals" required.
 */
export interface AllTicketsCompleteContext {
  channel: Channel;
  channelStore: ChannelStore;
  sessionId: string;
  tracker: TokenTracker;
  /**
   * Post-drain budget headroom percentage (0..100). Pre-computed so the
   * seam implementation doesn't have to care whether the tracker has
   * replayed its JSONL yet.
   */
  budgetHeadroomPct: number;
  /**
   * Audit invoker threaded through from `testOverrides.auditInvoker`, if
   * any. Production is `undefined` — AL-4's real driver is expected to
   * build one from the agent factory.
   */
  auditInvoker?: AuditInvoker;
}

/**
 * Autonomous-loop driver entrypoint. The behaviour is gated by two env
 * flags so AL-12's pool lifecycle and AL-14's worker drain can be rolled
 * out independently:
 *
 *   1. When {@link RELAY_REPO_ADMIN_POOL_ENABLED} is off:
 *      Preserves the pre-AL-12 behaviour — no pool, no routing, lifecycle
 *      transitions to `killed` with reason `"al-13-pending"`. This is the
 *      safe default.
 *   2. When only the pool flag is on:
 *      Boots the repo-admin pool (AL-12) and routes each ready ticket
 *      through {@link TicketRouter} (AL-13) so the matching admin queues
 *      it. Workers are NOT spawned and queues are NOT drained — the
 *      lifecycle transitions to `killed` with reason `"al-14-pending"`.
 *      This mode exercises boot + routing in production without leaking
 *      worktrees.
 *   3. When BOTH {@link RELAY_REPO_ADMIN_POOL_ENABLED} and
 *      {@link RELAY_AL14_WORKER_DRAIN} are on:
 *      Full AL-14 behaviour. For each admin whose session is live
 *      (`state === "ready"`), drain its pending queue via
 *      {@link TicketRunner} — spawn a worker in a per-ticket worktree,
 *      monitor until the worker exits, and mark the ticket `verifying`
 *      (PR opened) / `failed` (AC4) on the board. Serialized inside each
 *      admin; parallel across admins. On completion the lifecycle
 *      transitions to `killed` with reason `"al-16-pending"`.
 *   4. Tears down pool + lifecycle + tracker. Errors during teardown are
 *      logged and swallowed: a cosmetic shutdown failure must not turn a
 *      successful handoff into a non-zero CLI exit.
 *
 * ## Why two flags
 *
 * AL-14 owns spawn + drain but NOT PR-merge cleanup (AL-4's scope). A
 * ticket whose worker opens a PR transitions to `awaiting-merge` and its
 * worktree persists on disk until a merge event reaches
 * {@link TicketRunner.handlePrMerged}. Until AL-4 ships merge-detection
 * wiring, every pool-enabled autonomous run with the drain on leaks one
 * worktree per successful ticket. Gating the drain behind its own flag
 * lets operators opt into that leak knowingly — and keeps a clean opt-
 * out path for AL-12/AL-13 exercise runs that shouldn't spawn workers at
 * all.
 *
 * PR-merge cleanup (AC3) is NOT driven here — the runner exposes
 * {@link TicketRunner.handlePrMerged}; the autonomous-loop's steady-
 * state driver (AL-4) will plumb PR-poller merge events into that hook
 * once the loop runs long enough to observe them. AL-14's single-pass
 * autonomous stub intentionally exits before any PR can merge, so the
 * hook is dormant here.
 */
export async function startAutonomousSession(opts: StartAutonomousSessionOptions): Promise<void> {
  const { sessionId, lifecycle, tracker, channel, allowedRepos, testOverrides } = opts;

  // AL-12 built the repo-admin pool, but the admin-process handshake
  // protocol it relies on is AL-14's deliverable. Without that protocol
  // the default spawner's child exits in ms (no prompt, stdin closed)
  // and the pool flaps until the rapid-restart ceiling fires. Gate pool
  // construction behind RELAY_REPO_ADMIN_POOL_ENABLED so production
  // `rly run --autonomous` invocations keep the pre-AL-12 behaviour
  // (clean `killed / al-13-pending` exit) until AL-14 flips the flag.
  const poolEnabled = isRepoAdminPoolEnabled();
  // AL-14 spawn + drain is gated by a SEPARATE flag from the pool flag.
  // Until AL-4 ships merge-detection cleanup, every ticket that reaches
  // `awaiting-merge` leaves a worktree on disk — the two-flag split lets
  // operators opt into that leak explicitly rather than inheriting it
  // by flipping the pool flag. When the drain flag is off but the pool
  // flag is on, the loop still routes tickets (AL-13) but no workers
  // are spawned, and the lifecycle exits with reason `al-14-pending`.
  const drainEnabled = poolEnabled && isWorkerDrainEnabled();

  if (!poolEnabled) {
    console.log(
      `[autonomous-loop] dispatcher not yet implemented — ` +
        `Session ${sessionId} marked as killed with reason "al-13-pending".`
    );
  } else if (!drainEnabled) {
    console.log(
      `[autonomous-loop] repo-admin pool + router enabled; worker drain disabled ` +
        `(RELAY_AL14_WORKER_DRAIN unset) — Session ${sessionId} will route tickets and ` +
        `mark killed with reason "al-14-pending".`
    );
  } else {
    console.log(
      `[autonomous-loop] routing channel tickets + draining repo-admin queues — ` +
        `Session ${sessionId} will mark killed with reason "al-16-pending" once drained. ` +
        `worker drain enabled; tickets that reach awaiting-merge state will leave ` +
        `worktrees on disk until AL-4 lands merge-detection cleanup.`
    );
  }

  // AL-13/16: single channel store instance shared by the router, the
  // runner fleet, and the coordinator's decisions audit. Reusing one
  // instance avoids redundant on-disk state checks and keeps all
  // writes going through the same in-memory mirror.
  const channelStore = testOverrides?.channelStore ?? new ChannelStore();

  // AL-16: inter-admin coordination bus. Construct BEFORE the pool so
  // the pool can thread the reference into each session it spawns —
  // any in-process MCP handler built on a session's behalf then has
  // both coordinator and alias available at construction, which is
  // what `coordination_send` needs to route end-to-end. When the pool
  // is disabled (AL-13 pending path) the coordinator stays null; no
  // coordination is possible without admins to address anyway.
  //
  // The coordinator wraps a `pool` reference for alias-lookup on send;
  // we forward-declare a lazily-populated proxy that the pool writes
  // into after construction, so the ordering (coordinator-before-pool
  // for wiring, pool-before-use for routing) holds without a circular
  // constructor call. The proxy is a thin Pick<> view — the coordinator
  // only calls `getSession` / `listSessions` on it.
  let poolRef: RepoAdminPool | null = null;
  const poolView: Pick<RepoAdminPool, "getSession" | "listSessions"> = {
    getSession(alias: string) {
      return poolRef?.getSession(alias) ?? null;
    },
    listSessions() {
      return poolRef?.listSessions() ?? [];
    },
  };
  const coordinator = poolEnabled
    ? new Coordinator({
        pool: poolView,
        channelStore,
        channelId: channel.channelId,
      })
    : null;

  const pool = poolEnabled
    ? new RepoAdminPool({
        channel,
        allowedAliases: allowedRepos.map((r) => r.alias),
        fullAccess: channel.fullAccess ?? false,
        lifecycle,
        spawner: testOverrides?.repoAdminSpawner,
        rootDir: testOverrides?.rootDir,
        // AL-16: hand the shared bus to the pool so every session it
        // constructs receives the reference and can pass it into its
        // MCP handler at construction time.
        coordinator,
      })
    : null;
  poolRef = pool;

  if (pool) {
    try {
      await pool.start();
    } catch (err) {
      console.warn(
        `[autonomous-loop] repo-admin pool failed to boot: ${
          err instanceof Error ? err.message : String(err)
        }`
      );
    }
  }

  // AL-13: drain the channel's ticket board through the router exactly
  // once. AL-14 adds a second pass (gated by RELAY_AL14_WORKER_DRAIN)
  // that drains each admin's queue via TicketRunner — spawn a worker
  // per ticket, wait for it to open a PR (or fail), then exit. The
  // autonomous-loop's steady-state driver (AL-4) will replace this
  // single-pass shape; AL-14 just proves the end-to-end plumbing.
  let terminalReason: "al-13-pending" | "al-14-pending" | "al-16-pending" = "al-13-pending";
  if (pool) {
    terminalReason = drainEnabled ? "al-16-pending" : "al-14-pending";
    const runners: TicketRunner[] = [];
    try {
      const router = new TicketRouter({ pool, channel, channelStore });
      const boardTickets = await channelStore.readChannelTickets(channel.channelId);
      for (const ticket of boardTickets) {
        if (ticket.status !== "ready") continue;
        if (ticket.source === "linear") continue; // Linear mirror is read-only.
        try {
          const result = await router.route(ticket);
          if (result.kind === "unroutable") {
            console.warn(
              `[autonomous-loop] ticket ${ticket.ticketId} unroutable (${result.reason}); ` +
                `surfaced to channel board as blocked.`
            );
          }
        } catch (err) {
          console.warn(
            `[autonomous-loop] router threw on ticket ${ticket.ticketId}: ${
              err instanceof Error ? err.message : String(err)
            }`
          );
        }
      }

      if (drainEnabled) {
        // AL-14: drain each admin's pending queue in parallel. Each admin
        // serializes internally (one worker at a time in its repo); two
        // different admins DO run in parallel. The drain promise resolves
        // once every admin's queue is empty AND every in-flight worker has
        // exited.
        const spawner = testOverrides?.workerSpawner ?? new WorkerSpawner();
        // Filter to live admins only: `listSessions()` also returns admins
        // whose child has died / been terminally marked `stopped` by the
        // pool's rapid-restart ceiling. Dispatching into those sessions
        // is a programming error (they throw) and surfacing nothing is
        // the right behaviour — the router already wrote the blocked
        // status for the routing failure.
        const admins = pool.listSessions().filter((s) => s.state === "ready");
        for (const admin of admins) {
          const assignment = channel.repoAssignments?.find((a) => a.alias === admin.alias);
          if (!assignment) continue;
          runners.push(
            new TicketRunner({
              admin,
              repoAssignment: assignment,
              channel,
              channelStore,
              spawner,
            })
          );
        }
        await Promise.all(
          runners.map((r) =>
            r.drain().catch((err) => {
              console.warn(
                `[autonomous-loop] ticket runner drain failed: ${
                  err instanceof Error ? err.message : String(err)
                }`
              );
            })
          )
        );
      }
    } catch (err) {
      console.warn(
        `[autonomous-loop] ticket routing pass failed: ${
          err instanceof Error ? err.message : String(err)
        }`
      );
    } finally {
      // Ensure any in-flight worker is signalled before we unwind. Safe
      // to call even if drain() already finished — stop() is idempotent
      // and only hits still-running workers.
      await Promise.all(
        runners.map((r) =>
          r.stop("autonomous-loop-exit").catch((err) => {
            console.warn(
              `[autonomous-loop] runner stop failed: ${
                err instanceof Error ? err.message : String(err)
              }`
            );
          })
        )
      );
    }

    // AL-6: post-completion audit hook. Fires exactly once per session,
    // in the window between "last ticket drained" and "terminal lifecycle
    // transition". Gated internally on budget headroom + ledger health
    // (see `runPostCompletionAudit`). The `onAllTicketsComplete` seam is
    // exposed so AL-4's richer driver — which knows about retries,
    // stop-on-failure flags, etc. — can decide when "all tickets done"
    // truly holds; the default implementation treats "drain finished"
    // as the signal, which is correct for the current AL-14 single-pass
    // shape.
    //
    // Only fires when the worker drain was enabled. In drain-disabled
    // mode no tickets actually ran (AL-13 pending), so auditing what
    // "just completed" is meaningless and would write stale proposals
    // from a stale board.
    if (drainEnabled) {
      const headroomPct = Math.max(0, 100 - tracker.pct);
      const auditCtx: AllTicketsCompleteContext = {
        channel,
        channelStore,
        sessionId,
        tracker,
        budgetHeadroomPct: headroomPct,
        auditInvoker: testOverrides?.auditInvoker,
      };
      try {
        if (opts.onAllTicketsComplete) {
          await opts.onAllTicketsComplete(auditCtx);
        } else {
          await defaultOnAllTicketsComplete(auditCtx);
        }
      } catch (err) {
        // A misbehaving hook must NOT keep the loop from tearing down
        // its pool / lifecycle / tracker. Warn and continue.
        console.warn(
          `[autonomous-loop] onAllTicketsComplete hook failed: ${
            err instanceof Error ? err.message : String(err)
          }`
        );
      }
    }
  }

  // Only transition if we're still in a non-terminal state. A concurrent
  // threshold crossing or watchdog fire could theoretically have already
  // pushed us to `killed`; respect that and skip the transition in that
  // case to avoid throwing a LifecycleTransitionError the caller would
  // have to catch.
  const state = lifecycle.state;
  if (state !== "killed" && state !== "done") {
    try {
      await lifecycle.transition("killed", terminalReason);
    } catch (err) {
      console.warn(
        `[autonomous-loop] failed to transition lifecycle to killed: ${
          err instanceof Error ? err.message : String(err)
        }`
      );
    }
  }

  // AL-16: close the coordinator BEFORE stopping the pool. Order
  // matters — once `pool.stop()` starts, each session unwinds and
  // `getSession(to)` begins returning null, so a late-arriving
  // `coordination_send` would resolve with `no-such-admin` (generic,
  // confusing post-mortem) instead of the cleaner `coordinator-closed`
  // the close path produces. Closing first guarantees the in-flight
  // race resolves on the close rail.
  if (coordinator) {
    try {
      await coordinator.close();
    } catch (err) {
      console.warn(
        `[autonomous-loop] coordinator close failed: ${
          err instanceof Error ? err.message : String(err)
        }`
      );
    }
  }

  // Terminal lifecycle transition triggers the pool's auto-stop, but also
  // await it here so teardown happens before the CLI returns.
  if (pool) {
    try {
      await pool.stop();
    } catch (err) {
      console.warn(
        `[autonomous-loop] repo-admin pool stop failed: ${
          err instanceof Error ? err.message : String(err)
        }`
      );
    }
  }

  // Close out resources. Any in-flight writes drain here; errors are
  // warned but never thrown — the CLI exit path is the wrong place to
  // surface a teardown failure.
  try {
    await lifecycle.close();
  } catch (err) {
    console.warn(
      `[autonomous-loop] lifecycle close failed: ${
        err instanceof Error ? err.message : String(err)
      }`
    );
  }
  try {
    await tracker.close();
  } catch (err) {
    console.warn(
      `[autonomous-loop] tracker close failed: ${err instanceof Error ? err.message : String(err)}`
    );
  }
}

/**
 * AL-6: default implementation of the `onAllTicketsComplete` seam.
 * Reads the channel's current ticket ledger + decisions, then hands them
 * to {@link runPostCompletionAudit}. Logs the outcome so an operator can
 * tell from the autonomous-loop's final output whether the audit fired,
 * was gated off, or produced an invalid response.
 *
 * Auto-audit is disabled when no `auditInvoker` is supplied — i.e. the
 * production default until AL-4 wires the agent factory into this seam.
 * Tests explicitly pass an invoker via `testOverrides.auditInvoker`.
 */
async function defaultOnAllTicketsComplete(ctx: AllTicketsCompleteContext): Promise<void> {
  if (!ctx.auditInvoker) {
    // No invoker, no audit. Keeping this silent by default — the
    // autonomous-loop's pre-audit log already told the operator that
    // AL-4 is not yet wired; shouting "audit skipped (no invoker)" on
    // every successful run would just add noise.
    return;
  }

  const tickets = await ctx.channelStore.readChannelTickets(ctx.channel.channelId);
  const decisions = await ctx.channelStore.listDecisions(ctx.channel.channelId);

  const result: AuditRunResult = await runPostCompletionAudit({
    channel: ctx.channel,
    channelStore: ctx.channelStore,
    run: {
      sessionId: ctx.sessionId,
      tickets,
      decisions,
      // Recent git log capture is deferred to AL-4's richer driver,
      // which knows which repos the run touched. The audit prompt
      // tolerates an empty array, so skipping here produces no noise.
      recentCommits: [],
    },
    budgetHeadroomPct: ctx.budgetHeadroomPct,
    invokeAudit: ctx.auditInvoker,
  });

  if (result.kind === "fired") {
    console.log(
      `[autonomous-loop] audit-agent fired — ${result.proposalsWritten} proposal(s) written.`
    );
  } else if (result.kind === "skipped") {
    console.log(`[autonomous-loop] audit-agent skipped (${result.reason}).`);
  } else {
    console.warn(
      `[autonomous-loop] audit-agent returned invalid response (${result.issues.length} issue(s)).`
    );
  }
}
